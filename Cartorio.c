#include <stdio.h>       										// Biblioteca de comunicação com o usuário
#include <stdlib.h>      										// Biblioteca de alocação de espaço em memória
#include <locale.h>      										// Biblioteca de alocações de texto por região
#include <string.h>												// Biblioteca responsável por cuidar das strings

int registro()
{
	
	char arquivo[40];
	char cpf[40];
	char nome[40];
	char sobrenome[40];
	char cargo[40];
	
	printf ("Digite o CPF a ser cadastrado: ");
	scanf ("%s", cpf);
	
	strcpy (arquivo, cpf);										// Responsável por copiar os valores das strings
	
	FILE *file;													// Cria o arquivo
	file = fopen (arquivo, "w");								// Cria o arquivo
	fprintf (file, cpf);										// Salva o valor da variável	
	fclose (file);												// Fecha o arquivo
	
	file = fopen (arquivo, "a");								// Coloca uma virgula entre os dados
	fprintf (file, ",");
	fclose (file);
	
	printf ("Digite o nome a ser cadastrado: ");				// Mostra mensagem ao usuário
	scanf ("%s", nome);
	
	file = fopen (arquivo, "a");								// Recolhe o char "nome"
	fprintf (file, nome);
	fclose (file);
	
	file = fopen (arquivo, "a");								
	fprintf (file, ",");
	fclose (file);
	
	printf ("Digite o sobrenome a ser cadastrado: ");
	scanf ("%s", sobrenome);
	
	file = fopen (arquivo, "a");
	fprintf (file, sobrenome);
	fclose (file);
	
	file = fopen (arquivo, "a");
	fprintf (file, ",");
	fclose (file);
	
	printf ("Digite o cargo a ser cadastrado: ");
	scanf ("%s", cargo);
	
	file = fopen (arquivo, "a");
	fprintf (file, cargo);
	fclose (file);
	
	system ("pause");
	
	
}

int consulta()
{
	
	setlocale (LC_ALL, "Portuguese");							// Seleção de linguagem
	
	char cpf[40];
	char conteudo[200];
	
	printf ("Digite o CPF a ser consultado: ");
	scanf ("%s", cpf);
	
	FILE *file;
	file = fopen (cpf, "r");
	
	if (file == NULL)
	{
		printf ("Não foi possível achar o arquivo, não localizado!.\n");
		
	}
	
	while ( fgets ( conteudo, 200, file ) != NULL )
	{
		printf ("\nEssas são as informações do usuário: ");
		printf ("%s", conteudo);
		printf ("\n\n");
	}
	
	system ("pause");
	
	
	
	
}

int deletar()
{
	printf ("Você escolheu deletar nomes!\n");
	system ("pause");
}

int main()
{
	
	int opcao = 0;												// Definindo variáveis
	int laco = 1;
	
	for ( laco = 1 ; laco = 1 ;)
	{
	
		system ("cls");
		
		
		setlocale (LC_ALL, "Portuguese");						// Seleção de linguagem
	
		printf ("###  Cartório da EBAC  ###\n\n");				// Apresentação
		printf ("Escolha a opção desejada do menu: \n\n");		// Início do menu
		printf ("\t1 - Registrar nomes\n");
		printf ("\t2 - Consultar nomes\n");
		printf ("\t3 - Deletar nomes\n\n");
		printf ("Opção: \n\n");									// Fim do menu

		scanf ("%d", &opcao);									// Armazenamendo a escolha
	
		system ("cls");
	
		switch (opcao)											// Inicio da seleção
		{
			case 1:
			registro();
			break;
			
			case 2:
			consulta();
			break;
			
			case 3:
			deletar();
			break;
			
			
			default:
			printf ("Essa opção não está disponivel!\n");
			system ("pause");
			break;
		}														// Fim da seleção
	}
}
